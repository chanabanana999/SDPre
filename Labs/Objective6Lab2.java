public class Objective6Lab2 {
  public static void main(String[] args) {
    int counter = 0;

    System.out.println();

    // Note that the instructions say to print values 0 - 20, but the
    // example shows 1 - 20. The block below follows the instructions, not
    // the example/
    while ( counter <= 20 ) {
      System.out.println( counter);
      counter = counter + 1;
    }
    System.out.println();
  }
}
